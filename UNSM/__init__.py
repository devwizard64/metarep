def slidec(self, argv, data):
	size, p, c = struct.unpack(">4xIII", data[:16])
	s = 16
	buf = bytearray(size)
	i = 0
	bit = 0
	cnt = 0
	while i < size:
		if cnt == 0:
			bit = data[s]
			s += 1
			cnt = 8
		if bit & 0x80:
			buf[i] = data[c]
			i += 1
			c += 1
		else:
			x = data[p] << 8 | data[p+1]
			p += 2
			o = i - ((x & 0xFFF) + 1)
			n = (x >> 12) + 3
			while n > 0:
				buf[i] = buf[o]
				i += 1
				o += 1
				n -= 1
		bit <<= 1
		cnt -= 1
	return bytes(buf)

